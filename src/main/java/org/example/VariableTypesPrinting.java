package org.example;

public class VariableTypesPrinting {
    public static void main(String[] args) {
        int localvariable = 100;
        System.out.printf("This is the local variable %d",localvariable);
        String name = "Sandhya";
        System.out.printf("\n This is the local variable %s",name);
        boolean b = true;
        System.out.printf("\n The boolean variable value is %b",b);

    }
}
