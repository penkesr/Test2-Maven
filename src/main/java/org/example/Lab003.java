package org.example;

public class Lab003 {
    public static void main(String[] args) {
        int num = 24; // Number to print the table of

        // Loop from 1 to 10
        for (int i = 1; i <= 24; i++) {
            System.out.println(num + " x " + i + " = " + (num * i));
        }
    }
}
