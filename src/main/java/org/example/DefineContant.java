package org.example;

public class DefineContant {
    // Declare constants using static final
    public static final String FIRST_NAME = "Sandhya";      // Constant for first name
    public static final String LAST_NAME = "Penke";        // Constant for last name
    public static final double PERCENTAGE = 85.5;        // Constant for percentage

    public static void main(String[] args) {
        // Print the constant values
        System.out.println("First Name: " + FIRST_NAME);
        System.out.println("Last Name: " + LAST_NAME);
        System.out.println("Percentage: " + PERCENTAGE);
    }
}
