package org.example;

public class Lab0010_variables {
    public static void main(String[] args) {

        //variable with lowercase
        int age = 10;
        //variagle name followed by number
        int age12 = 30;
        //variagle name followed by $
        int age$ = 40;
        //variagle name followed by _
        int age_ = 50;
        //variagle name as _1
        int _1 =60;
        //variable with capital
        int NAME = 80;
        //variable with 2 names with $ in between
        int FN$LN = 90;

        System.out.println(" The value of age "+age);
        System.out.println(" The value of age12 "+age12);
        System.out.println(" The value of age$ "+age$);
        System.out.println(" The value of age_ "+age_);
        System.out.println(" The value of _1 "+_1);
        System.out.println(" The value of NAME "+NAME);
        System.out.println(" The value of FN$LN "+FN$LN);


    }
}
